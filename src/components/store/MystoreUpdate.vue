import {reactive} from "vue";
import axios from "axios";
import router from "@/scripts/router";
import Header2 from "@/components/header/Header2.vue";

export default {
  name: "MystoreUpdate",
  components: {Header2},
  data() {
    return {
      selectedFile: null,
      imageURL: "",
    };
  },
  methods: {
    onFileChange() {
      this.selectedFile = this.$refs.fileInput.files[0];
      const formData = new FormData();
      // const name = this.selectedFile.name;
      formData.append("file", this.selectedFile);
      axios.post("/api/images/upload", formData, {headers: {"Content-Type": "multipart/form-data",},}).then((data) => {
        this.state.form.storefile = data.data;
        this.downloadImage(data.data);
      });
    },
    downloadImage(fileId) {
      this.imageURL =`/api/images/download/${fileId}`;
    },
  },
  setup() {
    const state = reactive({
      form :{
        seq: "",
        id: "",
        password: "*************",
        name: "",
        email: "",
        tel: "",
        storenumber: "",
        storefile: ""
      }
    })
    const load = () => {
      axios.get("/api/store/mypage").then(({data}) => {
        state.form.seq = data.seq;
        state.form.id=data.id;
        // state.form.password=data.password;
        state.form.name=data.name;
        state.form.email=data.email;
        state.form.tel=data.tel;
        state.form.storenumber=data.storenumber;
        state.form.storefile=data.storefile;
      })
    };

    load();
    const mystoreupdate = () => {
      const updateData = {
        id: state.form.id,
        // password: state.form.password,
        name: state.form.name,
        email: state.form.email,
        tel: state.form.tel,
        storenumber: state.form.storenumber,
        storefile: state.form.storefile
      }
      axios.put("/api/store/mypage/update", updateData).then(()=> {
        window.alert("회원 정보가 수정 되었습니다");
        router.push({path: "/mystore_info"});
      })
    }

    const deleteStore = () => {
      axios.delete(`/api/store/mypage/delete/${state.form.seq}`).then(() => {
        window.alert("탈퇴 처리 되었습니다");
        router.push({path: "/"});
      })
    }

    return {state, mystoreupdate, deleteStore}
  }
}

